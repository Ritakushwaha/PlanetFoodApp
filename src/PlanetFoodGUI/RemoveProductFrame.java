    private void jcProdCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcProdCategoryActionPerformed
                
        if(jcProdCategory.getSelectedItem()==null)
         {
             return;
         }
        try
        {
        String catName=jcProdCategory.getSelectedItem().toString(); 
        String catId=categories.get(catName);
        productList=ProductsDao.getProductsByCategory(catId);
        jcProdId.removeAllItems();
        for(String prodId: productList.keySet())
                jcProdId.addItem(prodId);
        }
        catch(SQLException ex)
        {
          JOptionPane.showMessageDialog(null, "Error while connecting to DB!","Exception!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();  
        }
    }//GEN-LAST:event_jcProdCategoryActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        String prodId = jcProdId.getSelectedItem().toString();
        if(prodId==null){
            JOptionPane.showMessageDialog(null, "Please select a product id", "No selection", JOptionPane.ERROR);
            return;
        }
        else try {
            if(ProductsDao.deleteProduct(prodId)){
                JOptionPane.showMessageDialog(null, "Record Deleted Successfully", "Deletion Done", JOptionPane.INFORMATION_MESSAGE);
                for(String productId: productList.keySet())
                jcProdId.addItem(productId);
            }else{
                JOptionPane.showMessageDialog(null, "No such Record found for deletion", "Deletion Denied", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex, "ERROR", JOptionPane.ERROR);
            ex.printStackTrace();
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Required employee should be an integer","Employee Id must be Integer", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

